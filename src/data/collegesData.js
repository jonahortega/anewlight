// Helper function to get organizations for a specific college
export const getCollegeOrganizations = (collegeName) => {
  const college = collegesData.find(c => 
    c.name.toLowerCase().includes(collegeName.toLowerCase()) ||
    collegeName.toLowerCase().includes(c.name.toLowerCase())
  );
  
  if (college) {
    return {
      fraternities: college.fraternities,
      sororities: college.sororities
    };
  }
  
  return defaultOrganizations;
};

// Helper function to search colleges
export const searchColleges = (query) => {
  if (!query) return collegesData;
  
  return collegesData.filter(college => 
    college.name.toLowerCase().includes(query.toLowerCase()) ||
    college.state.toLowerCase().includes(query.toLowerCase())
  );
}; 